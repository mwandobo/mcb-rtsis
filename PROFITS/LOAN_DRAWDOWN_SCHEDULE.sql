    DAYS_AFTER        SMALLINT,
    DRAWDOWN_COMMENTS VARCHAR(250),
    ENTRY_STATUS      CHAR(1),
    TRX_USER          CHAR(8),
    TRX_DATE          DATE,
    TRX_TIMESTAMP     TIMESTAMP(6),
    UTILIZED_STATUS   CHAR(1),
    UTIL_TRX_UNIT     INTEGER,
