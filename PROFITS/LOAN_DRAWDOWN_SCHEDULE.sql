    ACCOUNT_SYSTEM    SMALLINT not null,
    DRAWDOWN_DATE     DATE     not null,
    DRAWDOWN_FULL     CHAR(1),
    DRAWDOWN_LIMIT    DECIMAL(15, 2),
    DRAWDOWN_USED     DECIMAL(15, 2),
