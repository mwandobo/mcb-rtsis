create unique index IXU_RAT_001
    on RATE_TABLE_FACTORS (FK_CURRENCYID_CURR, FK_GENERIC_DETAFK, FK_GENERIC_DETASER, FK_CUSTOMERCUST_ID,
                           EXCH_NOTES_FLAG, SCALE_MAX_AMOUNT);

